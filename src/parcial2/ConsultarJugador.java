    private void consultAction(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultAction
        // TODO add your handling code here:
        boolean exist = false;
        if (!jTextField1.getText().equals("")) {

            for (int i = 0; i < jugadores.length; i++) {
                if (jugadores[i][0] != null) {
                    if (jugadores[i][0].equals(jTextField1.getText())) {
                        jugadorSelected = jugadores[i];
                        exist = true;
                        break;
                    }
                }
            }

            if (exist) {
                this.jList1.setVisible(true);
                jList1.setModel(new javax.swing.DefaultComboBoxModel<>(cargarListado()));
            } else {
                JOptionPane.showMessageDialog(null, "Jugador no encontrado");
            }

        } else {
            JOptionPane.showMessageDialog(null, "Favor llenar todos los campos");
        }


    }//GEN-LAST:event_consultAction

