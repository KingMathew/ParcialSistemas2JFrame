    private String[] cargarListado() {
        String[] listadoEventosCombo = new String[]{
            "Nombre: " + jugadorSelected[1],
            "Posición: " + jugadorSelected[4],
            "Salario: " + jugadorSelected[3],
            "Bonificación por tiempo: " + jugadorSelected[7],
            "Bonificación por goles: " + jugadorSelected[8],
            "Total a pagar: " + jugadorSelected[9]};
        return listadoEventosCombo;
    }

