    private void actionConsult(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actionConsult
        boolean exist = false;
        if (!jTextField1.getText().equals("")) {

            for (int i = 0; i < jugadores.length; i++) {
                if (jugadores[i][0] != null) {
                    if (jugadores[i][0].equals(jTextField1.getText())) {
                        jugadorSelected = jugadores[i];
                        exist = true;
                        break;
                    }
                }
            }

            if (exist) {
                this.jPanel1.setVisible(true);

            } else {
                JOptionPane.showMessageDialog(null, "Jugador no encontrado");
            }

        } else {
            JOptionPane.showMessageDialog(null, "Favor llenar todos los campos");
        }

    }//GEN-LAST:event_actionConsult

    private void registerAction(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerAction

        if (!jTextField2.getText().equals("")
                && !jTextField3.getText().equals("")) {
            jugadorSelected[5] = jTextField2.getText();
            jugadorSelected[6] = jTextField3.getText();

            jugadorSelected[7] = getBonificacionTiempo(jTextField2.getText(), jugadorSelected[3]);
            jugadorSelected[8] = getBonificacionGoles(jTextField3.getText(), jugadorSelected[3]);
            jugadorSelected[9] = getTotalPagar(jugadorSelected[3]);

            JOptionPane.showMessageDialog(null, "Datos almacenados satisfactoriamente");
            Menu menu = new Menu();
            menu.cargarInfo(jugadores, contador);
            this.setVisible(false);
            menu.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Favor llenar todos los campos");
        }
    }//GEN-LAST:event_registerAction

