    private String getBonificacionTiempo(String tiempo, String salario) {

        int tiempoValue = Integer.valueOf(tiempo);
        double bonificacionValue = 0;

        if (tiempoValue < 90) {
            bonificacionValue = Integer.valueOf(salario) * 0.1;
        } else if (tiempoValue >= 90 && tiempoValue <= 180) {
            bonificacionValue = Integer.valueOf(salario) * 0.15;
        } else if (tiempoValue > 180) {
            bonificacionValue = Integer.valueOf(salario) * 0.2;
        }

        return String.valueOf(bonificacionValue);
    }

    private String getBonificacionGoles(String goles, String salario) {

        int golesValue = Integer.valueOf(goles);
        double bonificacionValue = 0;

        if (golesValue >= 1 && golesValue <= 5) {
            bonificacionValue = Integer.valueOf(salario) * 0.05;
        } else if (golesValue > 5) {
            bonificacionValue = Integer.valueOf(salario) * 0.1;
        }

        return String.valueOf(bonificacionValue);
    }
    
    private String getTotalPagar(String salario) {

       
        
        int total = Integer.valueOf(salario) + Double.valueOf(jugadorSelected[7]).intValue() + Double.valueOf(jugadorSelected[8]).intValue();
        

        return String.valueOf(total);
    }

